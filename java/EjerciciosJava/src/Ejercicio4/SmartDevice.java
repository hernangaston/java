package Ejercicio4;

public class SmartDevice {
    double with;
    double height;
    boolean camara;
    String procesador;

    public SmartDevice() {}

    public SmartDevice(double with, double height, boolean camara, String procesador) {
        this.with = with;
        this.height = height;
        this.camara = camara;
        this.procesador = procesador;
    }


}
