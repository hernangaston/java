package Ejercicio5;

public class Coche {
    int puertas;
    int ruedas;
    String marca;
    String modelo;

    public Coche (){}

    public Coche(int puertas, int ruedas, String marca, String modelo) {
        this.puertas = puertas;
        this.ruedas = ruedas;
        this.marca = marca;
        this.modelo = modelo;
    }
}
